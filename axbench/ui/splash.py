def should_show_splash(no_ui: bool) -> bool:
    if no_ui:
        return False
    if os.environ.get("CI"):
        return False
    if os.environ.get("AXBENCH_NO_UI") == "1":
        return False
    if os.environ.get("AXBENCH_FORCE_SPLASH") == "1":
        return True
    if os.environ.get("TERM", "").lower() == "dumb":
        return False
    return True


def render_splash(theme: UITheme = DEFAULT_THEME, unicode_ok: bool = True) -> Panel:
    subtitle = Text(theme.subtitle, style=f"bold {theme.accent}")
    hint = Text("Press Enter to begin", style=theme.muted)
    status = Text("Interactive console detected", style=theme.accent_alt)

    if unicode_ok:
        title = Text(ASCII_ART.strip("\n"), style=f"bold {theme.accent}")
    else:
        title = Text("AXBench", style=f"bold {theme.accent}")

    content = Group(
        Align.center(title),
        Align.center(subtitle),
        Align.center(Text("")),
        Align.center(status),
        Align.center(hint),
    )
    return Panel(
        content,
        title=f"[bold {theme.accent_alt}]AXBench Control Console[/bold {theme.accent_alt}]",
        border_style=theme.border,
        padding=(1, 4),
    )


def show_splash(console: Console, theme: UITheme = DEFAULT_THEME) -> None:
    console.clear()
    console.print(render_splash(theme=theme, unicode_ok=theme.unicode_ok))
    try:
        console.input()
    except (EOFError, KeyboardInterrupt):
        return
    console.clear()
